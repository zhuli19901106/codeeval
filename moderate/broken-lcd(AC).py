if __name__ == '__main__':	
	d = [
	'11111100', '01100000', '11011010', '11110010', '01100110', 
	'10110110', '10111110', '11100000', '11111110', '11110110'
	]
	d = [int(val, 2) for val in d]
	
	while True:
		try:
			s = raw_input()
		except EOFError:
			break
		a, b = re.split(';', s)
		b = list(b)
		try:
			dp = b.index('.') - 1
			b.remove('.')
		except ValueError:
			dp = len(b) - 1
		
		b = [int(val) for val in b]
		bc = len(b)
		
		a = [int(val, 2) for val in re.split(' ', a)]
		ac = len(a)
		
		suc = False
		i = 0
		while i + bc <= ac:
			j = 0
			while j < bc:
				if j == dp and (a[i + j] & 1 == 0):
					break
				if a[i + j] & d[b[j]] != d[b[j]]:
					break
				j += 1
			if j == bc:
				suc = True
				break
			i += 1
		print('1' if suc else '0')
